// Dato original

console.log("Datos originales", ejercicio);
console.log("Datos originales", typeof ejercicio);

// Dato number

ejercicio = Number(ejercicio);

console.log("Dato number", ejercicio);
console.log("Dato number", typeof ejercicio);

// Dato string

ejercicio = String(ejercicio);

console.log("Dato string", ejercicio);
console.log("Dato string", typeof ejercicio);

// Dato boolean

ejercicio = Boolean(ejercicio);

console.log("Dato boolean", ejercicio);
console.log("Dato boolean ", typeof ejercicio);

