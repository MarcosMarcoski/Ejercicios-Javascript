const firstYearCompleted = true;
const yearsCompleted = 1;

yearsCompleted == firstYearCompleted
yearsCompleted === firstYearCompleted


// == significa que tiene el mismo valor, por eso si lo ejecutamos en la consola saldrá que es verdadero, ya que True es 1 en el lenguaje binario.

console.log(yearsCompleted == firstYearCompleted); //true

// === significa estrictamente igual, por eso si lo ejecutamos en la consola nos dirá que es false, ya que no es literalmente lo mismo.

console.log(yearsCompleted === firstYearCompleted); //false